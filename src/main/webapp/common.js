		$("#date_wrap .date").text(date.getDate());       //현재 일정보 16 넣기 
		$("#date_wrap .hour").text(date.getHours());      //현재 시정보 12 넣기 
		$("#date_wrap .minute").text(date.getMinutes());  //현재 분정보 18 넣기 
