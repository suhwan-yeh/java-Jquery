/*
 주제 : 화면을 확대/축소하는 ZOOM 버튼 만들기
 ----------------------------------------------------------

 [+] 버튼 : 화면을 크게 만든다. (확대)
 [-] 버튼 : 화면을 작게 만든다. (축소)
 [100] 버튼 : 화면 크기를 원래 100% 상태로 되돌린다.

 ※ 화면 크기를 직접 바꾸려면 CSS에서 지원하는
    'zoom' 이나 'transform: scale()' 이라는 기능을 사용해야 한다.


 ----------------------------------------------------------
 [1] zoom 속성 (간단하게 화면 비율을 바꾸는 방법)
 ----------------------------------------------------------
 - 사용법 : $(선택한요소).css("zoom", "확대비율%");
 - 예시   : $("body").css("zoom", "200%");
            → body(전체 화면)을 200% 크기로 확대한다.

 - 특징 : 사용하기 매우 쉽다.
 - 단점 : Firefox(파이어폭스) 브라우저에서는 동작하지 않는다.
          (Chrome, Edge, Safari 등 대부분 브라우저에서는 잘 됨)


 ----------------------------------------------------------
 [2] transform: scale() 속성 (모든 브라우저에서 잘 동작하는 편)
 ----------------------------------------------------------
 - 사용법 : $(선택한요소).css("transform", "scale(비율)");
 - 예시   : $("body").css("transform", "scale(2)");
            → body(전체 화면)을 2배로 확대한다.

 - 특징 : Firefox 포함 거의 모든 브라우저에서 동작한다.
 - 주의 : 확대 기준점이 화면 중앙이라서,
          화면이 중심을 기준으로 퍼지듯이 확대된다.


 ----------------------------------------------------------
 [3] transform-origin 속성 (확대 기준점을 바꾸는 기능)
 ----------------------------------------------------------
 - 사용법 : $(선택한요소).css("transform-origin", "x값 y값");
 - 예시   : $("body").css("transform-origin", "0 0");
            → 화면의 왼쪽 위(0,0)에서부터 확대되도록 기준점을 변경한다.

 - 설명 : 기본적으로 scale()은 가운데를 기준으로 확대되는데,
          기준점을 바꾸면 왼쪽 위 또는 원하는 위치에서 확대되게 만들 수 있다.


 ----------------------------------------------------------
 정리
 ----------------------------------------------------------
 [+] 클릭 → 확대 (비율 증가)
 [-] 클릭 → 축소 (비율 감소)
 [100] 클릭 → 원래 크기(100%)로 되돌림

 zoom은 간단하지만 파이어폭스에서 안 되고,
 transform: scale()은 브라우저 호환성이 좋아서 많이 사용한다.
 */

  
/* zoom 버튼 */

//1. 확대 비율 초깃값 변수에 설정
let base = 100;

//2. <body></body> 전체 영역을 선택해서 변수에 저장
let mybody = $("body");

//3. <ul id="zoom"> 부모요소를 먼저 선택하고 후손선택자 빈공백을 이용하여 후손<a>요소 3쌍을 모두 배열에 담아 최종선택
//[ <a href="#" class="zoom_in">,          +      0 index
//  <a href="#" class="zoom_return">,     100     1 index
//  <a href="#" class="zoom_out">    ]     -      2 index
//그리고 click 이벤트를 각각 등록하자 
$("#zoom a").on("click", function(){
	
	// +   100   -  <a>태그들 중에서  'click'이벤트가 발생한 하나의 <a>의 index위치 번호 얻자
	let zNum = $("#zoom a").index(this);
	
	if(zNum === 0){  // + <a>를 클릭했다면?  =>  index 위치번호가 0인 칸에 저장된 <a>이면?
		
		base += 10; //확대값 10을 누적해서 증가시킨다.
		
	}else if(zNum === 1){  // 100 <a>를 클릭했다면?   =>   index 위치번호가 1인 칸에 저장된 <a>이면?
		
		base = 100; //정사이즈 100으로 다시 설정.
		
	}else{ // - <a> 를 클릭했다면?  => index 위치번호가 2인 칸에 저장된 <a>이면?
		
		base -= 10; //축소값 설정을 위해 -10 차감 
	}
	
	//<body></body> 전체 화면 영역을 선택해서 
	mybody
	//overflow-x속성을 적용하면 IE 8버전 이하에서 정상적으로 작동되도록 설정
	.css("overflow-x","auto")
	//body태그 전체 화면 영역의 확대 기준점을 x축과 y축의 좌표 0, 0으로 설정 하여 x축과 y축 방향으로 확대 되도록 설정
	.css("transform-origin", "0 0")
	//body태그 전체 화면 영역에 포함된 자식요소들 영역들이 base/100의 값만큼 확대되도록 설정
	.css("transform","scale("+ base/100 +")")
	//body태그 전체 화면 영역의 모든 태그가 base의 값 % 만큼 확대/축소 되도록 설정
	.css("zoom", base+"%");
	
	return false; //click 한  <a>태그의 href속성으로 설정된 기본이벤트 차단 
	
});


